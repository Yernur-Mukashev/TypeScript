// принимает строку из нескольких слов, разделённых пробелами,
// возвращает строку со словами в отзеркаленном виде, причём последовательность слов должна быть сохранена.
// Пример:
// "Это пример!" ==> "отЭ !ремирп"
// Советы и рекомендации
// Проверьте работу кода не только на фразе «Это пример!», но и на менее очевидных граничных случаях. Например, при вводе только пробелов без слов.

function mirrorWords(input: string): string {
    const words: string[] = input.trim().split(/\s+/); // Разбиваем строку на слова, удаляя лишние пробелы

    const mirroredWords: string[] = [];

    for (let i = 0; i < words.length; i++) {
        mirroredWords.push(words[i].split('').reverse().join('')); // Зеркалим текущее слово
    }
    
    return mirroredWords.join(' '); // // Объединяем зеркальные слова обратно в строку
}

console.log(mirrorWords("Это пример!")); // "отЭ !ремирп"
console.log(mirrorWords("  Привет мир  ")); // "тевирП рим"
console.log(mirrorWords("   ")); // ""
console.log(mirrorWords("Одно слово")); // "однО оловс"
console.log(mirrorWords("Проверка!")); // "!акреворП"

